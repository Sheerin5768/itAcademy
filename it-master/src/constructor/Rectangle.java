package constructor;

public class Rectangle {
    double width;
    double length;

    public Rectangle ( double w, double l) {
       width = w;
       length = l;
    }

    public void setWidth(double w) {
        width = w;
    }

    public void setLength(double l) {
        length = l;
    }

    double getWidth() {
        return width;
    }

    double getLength() {
        return length;
    }
}





